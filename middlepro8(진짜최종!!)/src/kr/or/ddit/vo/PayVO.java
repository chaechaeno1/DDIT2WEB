package kr.or.ddit.vo;

public class PayVO {

	private int rsv_no;
	private String pay_name;
	private String pay_date;
	private int pay_amt;
	private int refund_flag;
	
	
	public int getRsv_no() {
		return rsv_no;
	}
	public void setRsv_no(int rsv_no) {
		this.rsv_no = rsv_no;
	}
	public String getPay_name() {
		return pay_name;
	}
	public void setPay_name(String pay_name) {
		this.pay_name = pay_name;
	}
	public String getPay_date() {
		return pay_date;
	}
	public void setPay_date(String pay_date) {
		this.pay_date = pay_date;
	}
	public int getPay_amt() {
		return pay_amt;
	}
	public void setPay_amt(int pay_amt) {
		this.pay_amt = pay_amt;
	}
	public int getRefund_flag() {
		return refund_flag;
	}
	public void setRefund_flag(int refund_flag) {
		this.refund_flag = refund_flag;
	}
	@Override
	public String toString() {
		return "PayVO [rsv_no=" + rsv_no + ", pay_name=" + pay_name + ", pay_date=" + pay_date + ", pay_amt=" + pay_amt
				+ ", refund_flag=" + refund_flag + "]";
	}

	
	
	

}